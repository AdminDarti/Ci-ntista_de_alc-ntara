print("Aqui estão varios tipos de operações logicas em python\n")

# Operador AND
x = 5
y = 10
if x > 0 and y > 0:
    print("Ambos x e y são positivos")

# Operador OR
x = -5
y = 10
if x > 0 or y > 0:
    print("Pelo menos um dos números é positivo")

# Operador NOT
x = True
if not x:
    print("x é falso")
else:
    print("x é verdadeiro")
