print("Aqui estão varios tipos de operações em python\n")

# Operações aritméticas
x = 10
y = 3

# Adição
soma = x + y
print("Adição:", soma)  # Saída: 13

# Subtração
subtracao = x - y
print("Subtração:", subtracao)  # Saída: 7

# Multiplicação
multiplicacao = x * y
print("Multiplicação:", multiplicacao)  # Saída: 30

# Divisão
divisao = x / y
print("Divisão:", divisao)  # Saída: 3.3333333333333335

# Divisão inteira
divisao_inteira = x // y
print("Divisão Inteira:", divisao_inteira)  # Saída: 3

# Resto da divisão
resto_divisao = x % y
print("Resto da Divisão:", resto_divisao)  # Saída: 1

# Exponenciação
exponenciacao = x ** y
print("Exponenciação:", exponenciacao)  # Saída: 1000

