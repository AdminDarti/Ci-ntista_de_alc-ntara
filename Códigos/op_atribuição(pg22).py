print("Aqui estão varios tipos de operações em python\n")

# Atribuição simples
x = 10
print("Valor de x:", x)

# Atribuição com operador de adição
x += 5
print("Novo valor de x (x += 5):", x)

# Atribuição com operador de subtração
x -= 3
print("Novo valor de x (x -= 3):", x)

# Atribuição com operador de multiplicação
x *= 2
print("Novo valor de x (x *= 2):", x)

# Atribuição com operador de divisão
x /= 4
print("Novo valor de x (x /= 4):", x)

# Atribuição com operador de módulo
x %= 3
print("Novo valor de x (x %= 3):", x)

# Atribuição com operador de exponenciação
x **= 2
print("Novo valor de x (x **= 2):", x)
