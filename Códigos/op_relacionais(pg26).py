print("Aqui estão varios tipos de operações relacionais em python\n")


# Operador igual a
x = 5
y = 5
if x == y:
    print("x é igual a y")

# Operador diferente de
x = 5
y = 10
if x != y:
    print("x é diferente de y")

# Operador maior que
x = 10
y = 5
if x > y:
    print("x é maior que y")

# Operador menor que
x = 5
y = 10
if x < y:
    print("x é menor que y")

# Operador maior ou igual a
x = 10
y = 10
if x >= y:
    print("x é maior ou igual a y")

# Operador menor ou igual a
x = 5
y = 10
if x <= y:
    print("x é menor ou igual a y")
