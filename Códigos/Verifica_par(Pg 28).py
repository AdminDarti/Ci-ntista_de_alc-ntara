print("Aqui está um código para verificar se um numero é par ou não\n")

num = int(input("Digite o numero que você deseja verificar:"))

# utiliza-se de '%' para descobrir se a divisão de num por 2 é igual a 0, se sim prova-se que é par
if (num % 2) == 0: #Lembrando == é para indicar igualdade e = para atribuir variavel
    print("o numero é par")
else:
    print("o numero é impar")

 
